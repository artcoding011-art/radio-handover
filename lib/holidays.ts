/**
 * 매년 고정된 날짜의 공휴일인지 확인합니다.
 */
function getFixedHoliday(date: Date): string | null {
  const monthDay = format(date, 'MM-dd')
  switch (monthDay) {
    case '01-01': return '신정'
    case '03-01': return '삼일절'
    case '05-05': return '어린이날'
    case '06-06': return '현충일'
    case '08-15': return '광복절'
    case '10-03': return '개천절'
    case '10-09': return '한글날'
    case '12-25': return '성탄절'
    default: return null
  }
}

/**
 * 주어진 날짜가 대한민국 법정 공휴일인지 확인합니다.
 */
export function isKoreanHoliday(date: Date): boolean {
  return !!getHolidayName(date)
}

/**
 * 공휴일의 명칭을 가져옵니다.
 */
export function getHolidayName(date: Date): string | null {
  // 1. 매년 고정된 공휴일 확인
  const fixedName = getFixedHoliday(date)
  if (fixedName) return fixedName

  // 2. 고정되지 않은 공휴일(설날, 추석, 부처님오신날, 대체공휴일, 선거일 등) 확인
  const dateStr = format(date, 'yyyy-MM-dd')
  return HOLIDAY_DATA[dateStr] || null
}
